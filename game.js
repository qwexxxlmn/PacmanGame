// Создаем нового Пакмана
let createNewPacman = () => {
    pacman = new Pacman(
        oneBlockSize,
        oneBlockSize,
        oneBlockSize,
        oneBlockSize,
        oneBlockSize / 5
    );
};

// Главный игровой цикл
let gameLoop = () => {
    update();
    draw();
};

let gameInterval = setInterval(gameLoop, 1000 / fps);

// Перезапуск Пакмана и привидений
let restartPacmanAndGhosts = () => {
    createNewPacman();
    createGhosts();
};

// Обработчик столкновения с привидением
let onGhostCollision = () => {
    lives--; // Уменьшаем количество жизней
    restartPacmanAndGhosts(); // Перезапускаем Пакмана и привидений
    if (lives == 0) {
        // Если жизни закончились, завершить игру
        endGame();
    }
};

// Функция завершения игры
let endGame = () => {
    alert("Вы проиграли. Счет: " + score); // Выводим алерт с финальным счетом
    clearInterval(gameInterval); // Останавливаем игровой цикл
};

// Обновление состояния игры
let update = () => {
    pacman.moveProcess(); // Движение Пакмана
    pacman.eat(); // Пакман ест
    updateGhosts(); // Обновление привидений
    if (pacman.checkGhostCollision(ghosts)) { // Проверка на столкновение с привидением
        onGhostCollision();
    }
};

// Отрисовка еды
let drawFoods = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 2) { // Если еда
                createRect(
                    j * oneBlockSize + oneBlockSize / 3,
                    i * oneBlockSize + oneBlockSize / 3,
                    oneBlockSize / 3,
                    oneBlockSize / 3,
                    "#FEB897"
                );
            }
        }
    }
};

// Отрисовка оставшихся жизней
let drawRemainingLives = () => {
    canvasContext.font = "20px Emulogic";
    canvasContext.fillStyle = "white";
    canvasContext.fillText("Lives: ", 220, oneBlockSize * (map.length + 1));

    for (let i = 0; i < lives; i++) {
        canvasContext.drawImage(
            pacmanFrames,
            2 * oneBlockSize,
            0,
            oneBlockSize,
            oneBlockSize,
            350 + i * oneBlockSize,
            oneBlockSize * map.length + 2,
            oneBlockSize,
            oneBlockSize
        );
    }
};

// Отрисовка счета
let drawScore = () => {
    canvasContext.font = "20px Emulogic";
    canvasContext.fillStyle = "white";
    canvasContext.fillText(
        "Score: " + score,
        0,
        oneBlockSize * (map.length + 1)
    );
};

// Основная функция отрисовки
let draw = () => {
    canvasContext.clearRect(0, 0, canvas.width, canvas.height); // Очистка экрана
    createRect(0, 0, canvas.width, canvas.height, "black"); // Фон
    drawWalls(); // Отрисовка стен
    drawFoods(); // Отрисовка еды
    drawGhosts(); // Отрисовка привидений
    pacman.draw(); // Отрисовка Пакмана
    drawScore(); // Отрисовка счета
    drawRemainingLives(); // Отрисовка жизней
};

// Отрисовка стен
let drawWalls = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 1) { // Если стена
                createRect(
                    j * oneBlockSize,
                    i * oneBlockSize,
                    oneBlockSize,
                    oneBlockSize,
                    "#342DCA"
                );
                if (j > 0 && map[i][j - 1] == 1) { // Проверка соседних стен
                    createRect(
                        j * oneBlockSize,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth + wallOffset,
                        wallSpaceWidth,
                        wallInnerColor
                    );
                }

                if (j < map[0].length - 1 && map[i][j + 1] == 1) {
                    createRect(
                        j * oneBlockSize + wallOffset,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth + wallOffset,
                        wallSpaceWidth,
                        wallInnerColor
                    );
                }

                if (i < map.length - 1 && map[i + 1][j] == 1) {
                    createRect(
                        j * oneBlockSize + wallOffset,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth,
                        wallSpaceWidth + wallOffset,
                        wallInnerColor
                    );
                }

                if (i > 0 && map[i - 1][j] == 1) {
                    createRect(
                        j * oneBlockSize + wallOffset,
                        i * oneBlockSize,
                        wallSpaceWidth,
                        wallSpaceWidth + wallOffset,
                        wallInnerColor
                    );
                }
            }
        }
    }
};

// Функция для создания привидений
let createGhosts = () => {
    ghosts = [];
    for (let i = 0; i < ghostCount * 2; i++) {
        let newGhost = new Ghost(
            9 * oneBlockSize + (i % 2 == 0 ? 0 : 1) * oneBlockSize,
            10 * oneBlockSize + (i % 2 == 0 ? 0 : 1) * oneBlockSize,
            oneBlockSize,
            oneBlockSize,
            pacman.speed / 2,
            ghostImageLocations[i % 4].x,
            ghostImageLocations[i % 4].y,
            124,
            116,
            6 + i
        );
        ghosts.push(newGhost);
    }
};

createNewPacman();
createGhosts();
gameLoop();

// Обработчик нажатий клавиш для движения
window.addEventListener("keydown", (event) => {
    let k = event.keyCode;
    setTimeout(() => {
        if (k == 37 || k == 65) { // Стрелка влево или "a"
            pacman.nextDirection = DIRECTION_LEFT;
        } else if (k == 38 || k == 87) { // Стрелка вверх или "w"
            pacman.nextDirection = DIRECTION_UP;
        } else if (k == 39 || k == 68) { // Стрелка вправо или "d"
            pacman.nextDirection = DIRECTION_RIGHT;
        } else if (k == 40 || k == 83) { // Стрелка вниз или "s"
            pacman.nextDirection = DIRECTION_BOTTOM;
        }
    }, 1);
});
